/* ---------------------------------------------------------------
forever
==================================================================
无限循环 不需要任何条件，没有前置、后置语句
------------------------------------------------------------------
无限循环 只有循环体，一直无限循环下去 或者 在循环体中跳出
------------------------------------------------------------------*/

package main

import (
	"fmt"
	"time"
)

func main() {

	// 无限循环
	for {
		fmt.Println("time: ", time.Now())
	}
}
